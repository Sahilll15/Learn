# Create a list
my_list = [1, 2, 3, 4, 5]

# Append an element to the list
my_list.append(6)

# Print the list
print("List:", my_list)

# Access an element in the list
print("Element at index 2:", my_list[2])

# Create a tuple
my_tuple = (1, 2, 3, 4, 5)

# Access an element in the tuple
print("Element at index 3:", my_tuple[3])

# Create a set
my_set = {1, 2, 3, 4, 5}

# Add an element to the set
my_set.add(6)

# Print the set
print("Set:", my_set)

# Remove an element from the set
my_set.remove(3)

# Print the set
print("Set after removing 3:", my_set)
